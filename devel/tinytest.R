    t <- run_test_dir("devel/tinytest/", verbose=TRUE)
    print(t, limit=25, nlong=25)
} else {
    oldOptions <- options(warn=10)
    t <- run_test_dir("devel/tinytest/")
    print(t)
    do.call(options, oldOptions)
    rm(oldOptions)
}

